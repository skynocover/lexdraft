/**
 * briefType → 預設範本 ID 對照表
 * pipeline 中 template_id='auto' 時，根據 briefType 自動選擇最匹配的範本。
 * 目前系統僅有 4 種 briefType（complaint, defense, preparation, appeal），
 * 皆屬民事訴訟範疇。其餘範本（家事、勞動、強制執行等）需使用者手動選擇。
 */
const BRIEF_TYPE_TO_TEMPLATE: Record<string, string> = {
  complaint: 'default-01', // 民事訴訟 — 訴訟類
  defense: 'default-01', // 民事訴訟 — 訴訟類（答辯狀也在此範本）
  preparation: 'default-01', // 民事訴訟 — 訴訟類（準備書狀）
  appeal: 'default-03', // 民事訴訟 — 救濟類
};

/**
 * 根據 briefType 自動選擇最匹配的預設範本。
 * 如果沒有匹配，回傳 default-01（民事訴訟 — 訴訟類）作為 fallback。
 */
export const autoSelectTemplate = (briefType: string): DefaultTemplate => {
  const templateId = BRIEF_TYPE_TO_TEMPLATE[briefType] || 'default-01';
  return DEFAULT_TEMPLATES.find((t) => t.id === templateId) || DEFAULT_TEMPLATES[0];
};
